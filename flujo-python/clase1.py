# Estructuras condicionales

x = 5

#condicional palabra reservada if cerramos la idea con los :

if x > 5:
    print("X es mayor que 5") # si es verdadera entra al cuerpo del if
elif x == 5:
    print("X es igual que 5") # evalua otra condicion dentro del camino
else:
    print("X es menor que 5") # si es falsa entramos al cuerpo de else
print("Afuera")
